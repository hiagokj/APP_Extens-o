function gerarCurriculo() {
    document.getElementById("cvNome").textContent = document.getElementById("nome").value;
    document.getElementById("cvEmail").textContent = document.getElementById("email").value;
    document.getElementById("cvTelefone").textContent = document.getElementById("telefone").value;
    document.getElementById("cvExperiencia").textContent = document.getElementById("experiencia").value;
}
